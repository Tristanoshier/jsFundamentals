//equal 
console.log('3' == 3);

/*
    - coercion is the process of converting a value from one type to another
*/

//strict equal
console.log('3' === 3);
console.log(3 === 3);

//not equal
console.log('3' != 3);

//strict not equal 
console.log('3' !== 4);

//greater than
console.log(3 > 2);

//less than 
console.log( 2 < 3);

//greater than or equal to
console.log(3 >= 2);

//lesss than or equal to
console.log(2 <= 3);

//and 
2 && 3;

//or 
2 || 3;

